let input = prompt('Enter a number');
let num = +input;
if (isNaN(num) || input == null) {
    console.log ('Oops, you have made mistake. Please, try again.');
} else if (input % 2 == 0) {
    console.log ('It is even number');
} else {
    console.log ('It is an odd number');
}

